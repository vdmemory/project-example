export const getTipOfDayNext = (tipId: string, tips: TipOfDayType[]) => {
    const tipIndex = tips.findIndex(tip => tip.id === Number(tipId));
    if (tipIndex === tips.length - 1) {
        return tips[0];
    }
    return tips[tipIndex + 1];
};

export const getTipOfDayRandom = (tips: TipOfDayType[]) => {
    return tips[Math.floor(Math.random() * tips.length)];
};

